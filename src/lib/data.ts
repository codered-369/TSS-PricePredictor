const REDIS_KEY = 'tss_price_data';

export async function getPersistedData() {
  if (redis) {
    try {
      const data = await redis.get(REDIS_KEY);
      if (data && Array.isArray(data)) return data;
      await redis.set(REDIS_KEY, JSON.stringify(SEED_DATA));
      return SEED_DATA;
    } catch (e) {
      console.error("Redis read error", e);
    }
  }

  try {
    if (fs.existsSync(DATA_FILE)) {
      const saved = fs.readFileSync(DATA_FILE, 'utf-8');
      const parsed = JSON.parse(saved);
      if (parsed && parsed.length >= SEED_DATA.length) {
        return parsed;
      }
    }
  } catch(e) {}
  return [...SEED_DATA];
}

export async function savePersistedData(data: any[]) {
  if (redis) {
    try {
      await redis.set(REDIS_KEY, JSON.stringify(data));
      return;
    } catch (e) {
      console.error("Redis write error", e);
    }
  }

  try {
    fs.writeFileSync(DATA_FILE, JSON.stringify(data, null, 2));
  } catch(e) {}
}

